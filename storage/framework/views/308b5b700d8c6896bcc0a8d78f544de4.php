                        đăng ký kinh doanh của thương nhân, hoặc số, ngày cấp và đơn vị cấp quyết định thành lập của tổ
                        chức, hoặc mã số thuế cá nhân của cá nhân; số điện thoại hoặc một phương thức liên hệ trực tuyến
