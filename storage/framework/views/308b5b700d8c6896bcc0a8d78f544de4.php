                        Giao Dịch TMĐT OTOD.vn
                        - Chủ xe cập nhật các thông tin thay đổi về hàng hóa/dịch vụ đã đăng tải trên Sàn Giao Dịch TMĐT
                        OTOD.vn
                        - Chủ xe chịu trách nhiệm về chất lượng hàng hóa, dịch vụ cung cấp cho Khách hàng.
