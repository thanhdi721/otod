                        tin rao vặt và sử dụng các dịch vụ tiện ích trên Sàn giao dịch.
                        - Chủ xe có quyền đóng góp ý kiến cho Ban Quản lí trong quá trình hoạt động. Các kiến nghị được gửi
                        trực tiếp bằng thư, fax hoặc email đến cho Ban Quản lí.

