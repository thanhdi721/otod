                        đăng ký, mật khẩu và hộp thư điện tử của mình. Chủ xe có trách nhiệm thông báo kịp thời cho Ban Quản
                        lí về những hành vi sử dụng trái phép, lạm dụng, vi phạm bảo mật, lưu giữ tên đăng ký và mật khẩu
