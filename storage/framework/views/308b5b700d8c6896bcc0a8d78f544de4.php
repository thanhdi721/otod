                        III. Quy trình giao dịch
                        1. Quy trình dành cho người mua (khách hàng)

                        Để thuê xe trên Sàn giao dịch, Khách hàng vào website OTOD.vn và thực hiện các bước sau:

                        Bước 1: Khách hàng truy cập vào website OTOD.vn và đăng nhập vào tài khoản của mình, nếu Khách hàng
                        chưa có tài khoản thì có thể đăng ký tạo tài khoản bằng Email, Facebook hoặc tài khoản Google.

                        Bước 2: Tìm kiếm – Nhập địa điểm và khoảng thời gian Khách hàng muốn thuê xe, sau đó chọn tìm kiếm.

                        Bước 3: Chọn xe – Sau khi chọn tìm kiếm, trên website sẽ hiển thị các kết quả tìm kiếm, Khách hàng
                        sẽ thấy được danh sách xe phù hợp tiêu chí tìm kiếm cùng với hình ảnh xe, giá thuê, điểm rating của
                        các khách thuê xe trước đó. Khách hàng có thể điều chỉnh thời gian thuê và lọc kết quả tìm kiếm theo
                        các tiêu chí khác nhau để có được danh sách xe phù hợp theo ý Khách hàng. Sau đó Khách hàng lựa chọn
                        xe ưng ý để xem chi tiết hơn hình ảnh xe, mô tả xe, giá thuê xe, khuyến mãi, hình thức giao nhận xe,
                        thông tin Người bán (Chủ xe), điểm rating của xe và Chủ xe...

                        Bước 4: Đặt xe – Sau khi xem thông tin chi tiết xe và xác định được xe phù hợp, Khách hàng có thể
                        lựa chọn phương thức giao nhận và tiến hành Yêu cầu thuê xe. Khi đó, trên website sẽ hiển thị Thông
                        tin chuyến đi (Trip Summary), bao gồm chi tiết hành trình, thông tin xe, giá thuê cơ bản, khuyến
                        mãi, chi phí giao nhận xe và tổng số tiền. Bên dưới Bảng báo giá sẽ có mục trống để Khách hàng có
                        thể gửi lời nhắn đến Chủ xe. Chủ xe sau khi nhận được Yêu cầu thuê xe từ phía Khách hàng sẽ xem xét
                        và duyệt Yêu cầu thuê xe.

                        Sau khi Yêu cầu thuê xe được duyệt, khách hàng thực hiện thanh toán tiền cọc 40% tổng số tiền hiển
                        thị trên Thông tin chuyến đi. Sau khi đã đặt xe thành công, OTOD sẽ gửi biên nhận điện tử
                        (E-receipt) (xác nhận lại các thời gian hành trình, thông tin xe, giá thuê, số tiền đã đặt cọc, số
                        tiền còn lại cần thanh toán trực tiếp cho Chủ xe) cùng với Mã chuyến đi qua địa chỉ email của Khách
                        hàng. Chủ xe cũng sẽ nhận được biên nhận điện tử để bảo đảm sẽ thực hiện đầy đủ các thỏa thuận đã
                        cam kết với Khách hàng.

                        Trước thời điểm thuê xe 1-2 ngày, OTOD sẽ liên hệ với Khách hàng và Chủ xe để xác nhận lại các nội
                        dung trong biên nhận điện tử để đảm bảo việc thuê xe sẽ diễn ra như kế hoạch. Khi đến thời điểm thuê
                        xe, Khách hàng sẽ đến địa điểm của Chủ xe để nhận xe hoặc Chủ xe sẽ đưa xe đến trực tiếp địa chỉ của
                        Khách hàng tùy theo phương phức giao nhận Khách hàng đã lựa chọn khi đặt xe.

                        Trước khi giao xe, Khách hàng và Chủ xe sẽ kí Hợp đồng thuê xe và Biên bản bàn giao xe, Khách hàng
                        cần kiểm tra kĩ tình trạng xe (bên ngoài và nội thất) trước khi kí Biên bản bàn giao xe (ở mục "Tình
                        trạng xe khi nhận"). Hợp đồng thuê xe và Biên bản bàn giao xe được lập thành 2 bản, Chủ xe và Khách
                        hàng mỗi bên giữ 1 bản. Sau khi thực hiện xong Hợp đồng, Khách hàng cùng Chủ xe sẽ xác nhận lại tình
                        trạng xe. Khách hàng đề nghị Chủ xe kí vào Biên bản bàn giao xe (ở mục "Tình trạng xe khi hoàn trả")
                        để làm bằng chứng đã hoàn trả xe đúng nguyên trạng, dư phòng trong trường hợp có tranh chấp. Khách
                        hàng sau đó sẽ thanh toán số tiền còn lại với Chủ xe kể cả phần phát sinh.

                        2. Quy trình dành cho người bán (chủ xe)

                        Các thông tin đăng ký được yêu cầu phải đầy đủ, đúng chính tả, thống nhất với nhau, không vi phạm về
                        đăng tin nói chung. Thông tin đăng ký phải trung thực, rõ ràng, không gây nhầm lẫn hoặc hiểu lầm đối
                        với người xem về uy tín cho thuê xe cũng như các thông tin nhận biết khác. Để đăng ký thông tin xe
                        lên Sàn giao dịch, Người bán (Chủ xe) thực hiện theo hướng dẫn dưới đây:

                        Bước 1: Trước tiên người bán truy cập vào website https://www.otod.vn, click chuột vào nút "Đăng
                        kí". Hoặc có thể dùng các tài khoản Facebook, Google để đăng ký.

                        Bước 2: Tiến hành khai thông tin cá nhân theo mẫu hướng dẫn.

                        Bước 3: Đăng tải thông tin xe cho thuê

                        Chủ xe gửi thông tin xe cho thuê đến bộ phận kiểm duyệt nội dung để bộ phận này tiến hành đăng tải
                        thông tin hộ thì bộ phận kiểm duyệt thông tin sẽ tiến hành xác minh, xác thực lại nội dung sẽ đăng.
                        Những nội dung vi phạm bộ phận kiểm duyệt sẽ từ chối không đăng tải lên website.

                        3. Quy trình nhận dịch vụ

                        Khi đến thời điểm thuê xe, Khách hàng sẽ đến địa điểm của Chủ xe để nhận xe hoặc Chủ xe sẽ đưa xe
                        đến trực tiếp địa chỉ của Khách hàng tùy theo phương phức giao nhận Khách hàng đã lựa chọn khi đặt
                        xe.

                        Trước khi giao xe, Khách hàng và Chủ xe sẽ kí Hợp đồng thuê xe và Biên bản bàn giao xe, Khách hàng
                        cần kiểm tra kĩ tình trạng xe (bên ngoài và nội thất) trước khi kí Biên bản bàn giao xe.

                        Trong quá trình thực hiện nếu xảy ra tranh chấp thì Khách hàng và Chủ xe chủ động liên hệ với nhau
                        để giải quyết các vấn đề dựa trên các điều khoản của Hợp đồng thuê xe đã ký. Công ty sẽ hỗ trợ 2 bên
                        trong khả năng của mình. Công ty đề nghị các bên tham gia Sàn giao dịch khi tiến hành giao dịch phải
                        có ký kết Hợp đồng thuê xe và Biên bản bàn giao xe.

                        Sau khi thực hiện xong Hợp đồng thuê xe, Khách hàng cùng Chủ xe xác nhận lại tình trạng xe, cả 2 bên
                        cùng kí vào Biên bản bàn giao xe (ở mục "Tình trạng xe khi hoàn trả") và mỗi bên giữ lại 1 bản để
                        làm bằng chứng đã hoàn trả xe đúng nguyên trạng, dư phòng trong trường hợp có tranh chấp.

                        4. Quy trình thay đổi dịch vụ

                        Hệ thống Công ty không hỗ trợ tính năng thay đổi lịch trình, vì thế trong trường hợp khách hàng sau
                        khi đã đặt cọc và muốn thay đổi thời gian chuyến đi hay loại xe thì cần tiến hành hủy chuyến và đặt
                        lại chuyến mới.

                        5. Quy trình hủy dịch vụ

                        Khi muốn hủy chuyến đi đã đặt cọc, Khách hàng thực hiện theo các bước sau đây:

                        Bước 1: Thông báo tới Sàn giao dịch về việc thay đổi chuyến đi bằng các cách:

                        1.a Truy cập vào website OTOD.vn, đăng nhập vào tài khoản của Khách hàng, vào mục "Thông báo", chọn
                        "Sắp tới", Khách hàng sẽ thấy danh sách các chuyến đi sắp tới đã đã đặt cọc, chọn chuyến đi cần hủy,
                        chọn Hủy và xác nhận để gửi "Yêu cầu hủy chuyến" đến chủ xe.

                        1.b Gọi điện đến Bộ phận Chăm sóc Khách hàng tại số điện thoại 1900 368686 để được hỗ trợ.

                        Bước 2: Bộ phận Chăm sóc Khách hàng sẽ liên hệ với phía Chủ xe, xác nhận thông tin hủy chuyến và
                        tính phí vi phạm (nếu có), gửi thông tin qua mail cho Khách hàng đồng thời gửi thông tin tới Chủ xe.

                        Bước 3: Hoàn trả tiền cho Khách hàng sau khi cấn trừ tiền đặt cọc với phí hủy chuyến và phí chuyển
                        khoản.

                        6. Quy trình giải quyết khiếu nại

                        Công ty và Chủ xe có trách nhiệm tiếp nhận các khiếu nại và hỗ trợ Khách hàng liên quan đến các giao
                        dịch được kết nối thông qua Sàn giao dịch. Các khiếu nại liên quan đến việc cung cấp, sử dụng dịch
                        vụ thuê xe trên Sàn giao dịch do Công ty chịu trách nhiệm độc lập giải quyết trên cơ sở quy định của
                        pháp luật, Điều khoản và Điều kiện sử dụng dịch vụ, các thông báo, quy chế đã công bố với Thành viên
                        (Khách hàng và Chủ xe). Khi phát sinh tranh chấp, Công ty đề cao giải pháp thương lượng, hòa giải
                        giữa các bên nhằm duy trì sự tin cậy của Thành viên vào chất lượng dịch vụ của Sàn giao dịch. Khách
                        hàng có thể thực hiện theo các bước sau:

                        Bước 1: Khách hàng khiếu nại về dịch vụ qua số điện thoại 1900 368686 hoặc gửi mail cho Bộ phận Chăm
                        sóc Khách hàng tại địa chỉ admin@otod.vn. Thời gian để Công ty tiếp nhận khiếu nại là 3 ngày kể từ
                        ngày sử dụng dịch vụ hoặc từ ngày phát sinh sự việc.

                        Bước 2: Trong thời hạn (3) ngày làm việc kể từ khi tiếp nhận thông tin khiếu nại của Khách hàng, Bộ
                        phận Chăm sóc Khách hàng xác nhận thông tin khiếu nại, tiến hành phân loại thông tin và thông báo
                        cho Khách hàng:

                        2a. Ghi nhận các yêu cầu và các khiếu nại có liên quan đến Công ty và trong thời hạn khiếu nại.

                        2b. Từ chối các yêu cầu, các khiếu nại không có liên quan đến Công ty và hết thời hạn khiếu nại.

                        Bước 3: Giải quyết vấn đề

                        Bộ phận Chăm sóc Khách hàng sẽ tiến hành xác minh, kiểm chứng và phân tích tính chất và mức độ của
                        nội dung khiếu nại, phạm vi khiếu nại và trách nhiệm xử lý để phối hợp với Chủ xe và Bên cung cấp
                        dịch vụ thứ 3 đưa ra biện pháp cụ thể để hỗ trợ Khách hàng giải quyết tranh chấp đó.

                        3a. Chuyển các vấn đề có liên quan trực tiếp đến Công ty cho các Bộ phận có liên quan kiểm tra và
                        đối chiếu.

                        3b. Chuyển các vấn đề có liên quan cho Chủ xe giải quyết.

                        Trong thời hạn ba (3) ngày làm việc kể từ khi tiếp nhận thông báo về khiếu nại, Chủ xe có trách
                        nhiệm phối hợp với OTOD để giải quyết, xử lý khiếu nại. Chủ xe sẽ thông báo cho Khách hàng biện pháp
                        xử lý hoặc ủy quyền thông báo cho Công ty.

                        Bước 4: Đóng khiếu nại

                        4a. Khách hàng đồng ý với các phản hồi của Bộ phận Chăm sóc Khách hàng -> Kết thúc khiếu nại. Khách
                        hàng không đồng ý -> Quay lại bước 3

                        4b. Theo dõi các giải quyết khiếu nại của Chủ xe -> Kết thúc khiếu nại khi Khách hàng và Chủ xe đã
                        thỏa thuận xong.

                        Khi nhận được thông báo về biện pháp xử lý từ Chủ xe nhưng Khách hàng không đồng ý thì Công ty sẽ
                        chủ trì việc thương lượng, hòa giải giữa Khách hàng và Chủ xe để đi đến kết quả giải quyết phù hợp
                        với cả hai bên. Trong trường hợp Khách hàng và Chủ xe không đi đến thỏa thuận chung hoặc Khách hàng
                        không đồng ý với những biện pháp giải quyết cuối cùng của Chủ xe và/hoặc nằm ngoài khả năng và thẩm
                        quyền của Công ty thì Khách hàng hoặc Chủ xe có thể nhờ đến Cơ quan Nhà nước có thẩm quyền can thiệp
                        và giải quyết theo Pháp luật nhằm đảm bảo lợi ích hợp pháp của các bên.

                        Công ty tôn trọng và nghiêm túc thực hiện các quy định của Pháp luật về Bảo vệ quyền lợi của người
                        dùng. Công ty khuyến nghị Khách hàng và Chủ xe cung cấp chính xác, trung thực, chi tiết các thông
                        tin cá nhân và nội dung đăng tin liên quan đến dịch vụ. Chúng tôi cũng đề nghị Chủ xe cần nghiêm túc
                        tuân thủ các quy định của Pháp luật, cũng như có những hành vi phù hợp đối với Khách hàng. Mọi hành
                        vi lừa đảo, gian lận trong kinh doanh, gây tổn hại đến người khác đều bị lên án và phải chịu hoàn
                        toàn trách nhiệm trước Pháp luật. Các bên bao gồm Khách hàng và Chủ xe sẽ phải có trách nhiệm tích
                        cực trong việc giải quyết khiếu nại. Chủ xe cần có trách nhiệm chủ động xử lý và cung cấp văn bản
                        giấy tờ chứng thực thông tin liên quan đến sự việc đang có khiếu nại, tranh chấp với Khách hàng.
                        Công ty chỉ đóng vai trò hỗ trợ, phối hợp việc thương lượng, hòa giải và giải quyết khiếu nại giữ
                        Khách hàng và Chủ xe. Công ty cũng có trách nhiệm cung cấp những thông tin liên quan đến Khách hàng
                        và Chủ xe nếu được Chủ xe hoặc Khách hàng hoặc Cơ quan Pháp luật có thẩm quyền yêu cầu.

                        Sau khi Khách hàng và Chủ xe đã giải quyết xong tranh chấp, cần có trách nhiệm báo lại cho Công ty
                        để cập nhật tình hình. Trong trường hợp giao dịch phát sinh mâu thuẫn mà lỗi thuộc về Chủ xe: Công
                        ty sẽ áp dụng các biện pháp xử lý vi phạm tương ứng bao gồm nhưng không giới hạn: cảnh cáo, khóa tài
                        khoản hoặc chuyển cho Cơ quan Pháp luật có thẩm quyền tùy theo mức độ của sai phạm. Công ty sẽ chấm
                        dứt và gỡ bỏ toàn bộ tin bài về sản phẩm, dịch vụ của Chủ xe đó trên Sàn giao dịch đồng thời yêu cầu
                        Chủ xe bồi hoàn cho Khách hàng thỏa đáng trên cơ sở thỏa thuận với Khách hàng.

                        IV. Đảm bảo an toàn giao dịch
                        Công ty đã sử dụng các dịch vụ để bảo vệ thông tin về nội dung mà Chủ xe đã đăng về xe cho thuê nhằm
                        đảm bảo các giao dịch được tiến hành thành công, hạn chế tối đa rủi ro có thể phát sinh.

                        Chủ xe phải cung cấp thông tin đầy đủ (tên, địa chỉ, số điện thoại, email) trong mỗi gian hàng xe
                        cho thuê.

                        Khách hàng và Chủ xe không nên đưa thông tin chi tiết về việc thanh toán với bất kỳ ai bằng email
                        hoặc hình thức liên lạc khác, Công ty không chịu trách nhiệm về những thiệt hại hay rủi ro mà Thành
                        viên (Khách hàng và Chủ xe) có thể gánh chịu trong việc trao đổi thông tin của Thành viên qua
                        Internet hoặc email.

                        Trong trường hợp Khách hàng liên hệ trực tiếp với Chủ xe và không sử dụng dịch vụ của Công ty thì
                        Khách hàng phải cân nhắc cẩn thận việc giao tiền trước cho Chủ xe.

                        Thành viên tuyệt đối không sử dụng bất kỳ chương trình, công cụ hay hình thức nào khác để can thiệp
                        vào hệ thống hay làm thay đổi cấu trúc dữ liệu. Nghiêm cấm việc phát tán, truyền bá hay cổ vũ cho
                        bất kỳ hoạt động nào nhằm can thiệp, phá hoại hay xâm hại hệ thống của Công ty. Mọi vi phạm sẽ bị xử
                        lý theo Quy chế và Quy định của Pháp luật.

                        Mọi thông tin giao dịch đều được bảo mật an toàn, trừ trường hợp buộc phải cung cấp khi Cơ quan Pháp
                        luật yêu cầu.

