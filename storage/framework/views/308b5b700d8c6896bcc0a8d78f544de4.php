                        nhận được phản ánh về hành vi kinh doanh vi phạm pháp luật trên sàn giao dịch thương mại điện tử. Hỗ
                        trợ Cơ quan Quản lý nhà nước điều tra các hành vi kinh doanh vi phạm pháp luật, cung cấp thông tin
