    <script>
        (function() {
            const buttons = document.querySelectorAll('.clause-tabs .nav-link');
            const panes = document.querySelectorAll('.tab-pane');
            buttons.forEach(btn => {
                btn.addEventListener('click', () => {
                    const target = btn.getAttribute('data-target');
                    // active button
                    buttons.forEach(b => b.classList.remove('active'));
                    btn.classList.add('active');
                    // show pane
                    panes.forEach(p => {
                        if (p.id === target.substring(1)) {
                            p.classList.add('show', 'active');
                        } else {
                            p.classList.remove('show', 'active');
                        }
                    });
                });
            });

            // Format long clause text into paragraphs and bold headings
            const longBlocks = document.querySelectorAll('.clause-long:not(.tax-raw)');
            longBlocks.forEach(block => {
                const raw = block.innerText; // nguồn là text thuần
                const lines = raw.split(/\n+/).map(l => l.trim()).filter(Boolean);
                const frag = document.createDocumentFragment();
                const headingRegex = /^(I{1,3}|IV|V|VI{0,3}|X|XI|XII)\.|^\d+\.|^Bước\s*\d+:/i;
                lines.forEach(line => {
                    const p = document.createElement('p');
                    p.textContent = line;
                    if (headingRegex.test(line)) p.classList.add('heading');
                    frag.appendChild(p);
                });
                block.innerHTML = '';
                block.appendChild(frag);
            });

        })();
    </script>
