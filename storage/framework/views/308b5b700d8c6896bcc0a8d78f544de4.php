                        bản quyền nội dung, hình ảnh, quá trình giao dịch với các đối tác trên Sàn giao dịch và chịu trách
                        nhiệm về tính chính xác, trung thực của thông tin về sản phẩm/dịch vụ được trao đổi mua bán trên Sàn
                        giao dịch.

