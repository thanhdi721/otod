                        Chủ xe phải giải trình về nhũng thông tin đó. Tùy theo mức độ sai phạm, Ban Quản lí sẽ có phương án
                        giải quyết yêu cầu Chủ xe đền bù cho Khách hàng, sẽ khóa tài khoàn Thành viên của Chủ xe; Ban Quản
